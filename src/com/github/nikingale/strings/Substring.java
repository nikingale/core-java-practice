public class Substring {

    public static void main(String[] args) {
        String string = "kilimanjaro";

        System.out.println("String - " + string);
        System.out.println("Substring(4) - " + string.substring(4));
        System.out.println("Substring(4, 7) - " + string.substring(4, 7));
    }

}